package collections.lists;

public class Book {
	 int id;  
	    public String name;
		public String author;
		String publisher;  
	    int quantity;
	    
	    //constructor
	    public Book(int id, String name, String author, String publisher, int quantity)
	    {  
	        this.id = id;  
	        this.name = name;  
	        this.author = author;  
	        this.publisher = publisher;  
	        this.quantity = quantity;  
	    }

}
